import { createContext, useContext, useState, useEffect, ReactNode } from 'react';
import { useAuth } from '@/hooks/use-auth';

export type AppLanguage = 'en' | 'de' | 'fr' | 'vi' | 'ja' | 'zh';

interface AppI18nContextType {
  language: AppLanguage;
  setLanguage: (lang: AppLanguage) => void;
  t: (key: string) => string;
}

const AppI18nContext = createContext<AppI18nContextType | undefined>(undefined);

export function AppI18nProvider({ children }: { children: ReactNode }) {
  const { user } = useAuth();
  const [language, setLanguageState] = useState<AppLanguage>('en');

  // Sync language with user's primaryLanguage from database
  useEffect(() => {
    if (user?.primaryLanguage && ['en', 'de', 'fr', 'vi', 'ja', 'zh'].includes(user.primaryLanguage)) {
      setLanguageState(user.primaryLanguage as AppLanguage);
    }
  }, [user?.primaryLanguage]);

  const setLanguage = (lang: AppLanguage) => {
    setLanguageState(lang);
  };

  const t = (key: string): string => {
    const translations = appTranslations[language];
    const keys = key.split('.');
    let value: any = translations;
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    return typeof value === 'string' ? value : key;
  };

  return (
    <AppI18nContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </AppI18nContext.Provider>
  );
}

export function useAppI18n() {
  const context = useContext(AppI18nContext);
  if (!context) {
    throw new Error('useAppI18n must be used within AppI18nProvider');
  }
  return context;
}

